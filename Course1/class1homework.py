
def sum(n):
    Result = 0
    Addition_number=1
    while Addition_number < n+1:
        Result = Result + Addition_number
        Addition_number+=1
    return Result


param = 100
print ('1加到'+str(param)+'的结果是'+ str(sum(param)))

